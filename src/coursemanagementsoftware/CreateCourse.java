    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String coursename = CourseName.getText();
        if(coursename.isEmpty()){
            JOptionPane.showMessageDialog(cc, "Enter Course Name");
            return;
        }
        String date = null;
        try{
            Date.getDate().toString();
            date = Date.getEditor().getText();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(cc, "Please write date in proper format");
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String datecur = sdf.format(new Date());
        Date curdate = null,coursedate = null;
        try {
            curdate = sdf.parse(datecur);
            coursedate = sdf.parse(date);
        } 
        catch (ParseException ex) {
            Logger.getLogger(CreateCourse.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        int diffindays;
        diffindays = (int) ((coursedate.getTime() - curdate.getTime()) / (1000 * 60 * 60 * 24));
        if(diffindays < 0 || diffindays > 365*5){
            JOptionPane.showMessageDialog(cc, "Enter a date within next 5 years");
            return;
        }
        int courseyear = Integer.parseInt(date.split("/")[2]);
        int curyear = Calendar.getInstance().get(Calendar.YEAR);
        int index = courseyear - curyear;
        int size = cpar.get(index).size();
        if(size>10){
            JOptionPane.showMessageDialog(cc, "10 courses already registered for session " + courseyear + "-" +(courseyear+1));
            return;
        }
        Integer dur;
        try{
            dur = Integer.parseInt(Duration.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(cc, "Enter an integer for duration");
            Duration.setText("");
            return;
        }
        if(dur<1 || dur > 14){
            JOptionPane.showMessageDialog(cc, "Enter an integer between 0 and 15");
            Duration.setText("");
            return;
        }
        Integer fees;
        try{
            fees = Integer.parseInt(Fees.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(cc, "Enter an integer for Fees");
            Fees.setText("");
            return;
        }
        String coordname = CoordName.getText();
        if(coordname.isEmpty()){
            JOptionPane.showMessageDialog(cc, "Enter Coordinator Name");
            return;
        }
        String dep = Department.getText();
        if(dep.isEmpty()){
            JOptionPane.showMessageDialog(cc, "Enter Department");
            return;
        }
        String address = Address.getText();
        if(address.isEmpty()){
            JOptionPane.showMessageDialog(cc, "Enter Address");
            return;
        }
        String mobile = MobileNo.getText();
        if(mobile.isEmpty()){
            JOptionPane.showMessageDialog(cc, "Enter Mobile No");        
            return;
        }
        if(!MobileNoChecker(mobile) || mobile.length() < 6 ||  mobile.length() > 15 ){
            JOptionPane.showMessageDialog(cc, "Enter a valid mobile no (6 - 15 digits)");
            return;
        }
        String email = Email.getText();
        if(email.isEmpty()){
            JOptionPane.showMessageDialog(cc, "Enter Email");   
            return;
        }
        if(!email.contains("@")){
            JOptionPane.showMessageDialog(cc, "Enter a valid email");
            return;
        }
        Courses c = new Courses();
        c.setName(coursename);
        c.setDate(date);
        c.setDuration(dur);
        c.setFee(fees);
        c.getFaculty().setName(coordname);
        c.getFaculty().setDepartment(dep);
        c.getFaculty().setAddress(address);
        c.getFaculty().setMobileNo(mobile);
        c.getFaculty().setEmail(email);
        cpar.get(index).add(c);
        cc.dispose();
        mg.displayCourses();
        mg.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

