    public boolean validDate(String Date){
        String pattern = "[0-3][0-9]/[0-1][0-9]/[0-9]{4}";
        Pattern r = Pattern.compile(pattern);
        Matcher m = r.matcher(Date);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        if(!m.find()){
            JOptionPane.showMessageDialog(cd, "Enter a valid date");
            return false;
        }
        else
            return true;
    }

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Courses o = cpar.get(sindex).get(index);
        String coursename = name.getText();
        if(coursename.isEmpty()){
            JOptionPane.showMessageDialog(cd, "Enter Course Name");
            return;
        }
        String date = null;
        try{
            date = Date.getEditor().getText();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(cd, "Please write date in proper format");
            return;
        }
        if(!validDate(date))
            return;
        int courseyear = Integer.parseInt(date.split("/")[2]);
        int session = Integer.parseInt(o.getDate().split("/")[2]);
        if(session!=courseyear){
            JOptionPane.showMessageDialog(cd, "You cannot change course session");
            return;
        }
        Integer dur;
        try{
            dur = Integer.parseInt(duration.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(cd, "Enter an integer for duration");
            duration.setText("");
            return;
        }
        if(dur<1 || dur > 14){
            JOptionPane.showMessageDialog(cd, "Enter an integer between 0 and 15");
            duration.setText("");
            return;
        }
        Integer fees;
        try{
            fees = Integer.parseInt(Fees.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(cd, "Enter an integer for Fees");
            Fees.setText("");
            return;
        }
        o.setName(coursename);
        o.setDate(date);
        o.setDuration(dur);
        o.setFee(fees);
        cd.dispose();
        mg.setVisible(true);
        mg.displayCourses();
    }//GEN-LAST:event_jButton4ActionPerformed

