    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Courses c = cpar.get(sindex).get(index);
        Object[] possibilities = new Object[c.getFacs().size()];
        for(int i = 0; i < c.getFacs().size(); i++){
            possibilities[i] = i + 1 + " " + c.getFacs().get(i).getName();
        }
        if(c.getFacs().isEmpty()){
            JOptionPane.showMessageDialog(cd, "No Instructors present");
            return;
        }
        String s = (String)JOptionPane.showInputDialog(
                            cd,
                            "Choose Instructor",
                            "Input",
                            JOptionPane.PLAIN_MESSAGE,
                            null,
                            possibilities,
                            "ham");
        if(s==null)
            return;
        int k = Integer.parseInt(s.split(" ")[0]) - 1;
        Change w = new Change();
        w.setNum(k);
        cd.setVisible(false);
        w.setMode(4);
        w.setMg(mg);
        w.setCd(cd);
        w.setCmn(w);
        w.setCpar(cpar);
        w.setSindex(sindex);
        w.setIndex(index);
        w.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

